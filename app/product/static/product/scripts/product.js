class Product {
	productName;
	productCostPrice;
	productSellingPrice;
	productQuantity;

	constructor(productName, productQuantity, productCostPrice, productSellingPrice) {
		this.productName = productName;
		this.productQuantity = productQuantity;
		this.productCostPrice = productCostPrice;
		this.productSellingPrice = productSellingPrice;
	}
}
